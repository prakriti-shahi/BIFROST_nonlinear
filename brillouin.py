def install_brillouin_methods(fiber_obj):
    """
    Attach Brillouin noise methods to an existing FiberLength instance.

    After calling this, the object gains:
        fiber.calcSpBrilNoise(lambda_ref, lambda_quantum, delta_lambda, P_ref)
        fiber.brillouinThreshold(lambda_ref=1550e-9)
        fiber.checkSBSThreshold(P_pump, lambda_ref=1550e-9)

    Parameters
    ----------
    fiber_obj : FiberLength instance
        Must have .Aeff, .Lt or .L0, and .T0 attributes.

    Returns
    -------
    fiber_obj  (same object, with new methods attached)

    Examples
    --------
    >>> import fibers, brillouin
    >>> f = fibers.FiberLength(...)
    >>> brillouin.install_brillouin_methods(f)
    >>> thresh = f.brillouinThreshold()
    >>> noise  = f.calcSpBrilNoise(1550e-9, 1550.09e-9, 0.1e-9, 1e-3)
    """
    import types

    def _calcSpBrilNoise(self, lambda_ref, lambda_quantum, delta_lambda,
                          P_ref, **kwargs):
        T_K = getattr(self, 'T0', 22.0) + 273.15
        L   = getattr(self, 'Lt', getattr(self, 'L0', None))
        return spBril_noise_in_channel(
            lambda_pump=lambda_ref, lambda_channel=lambda_quantum,
            delta_lambda=delta_lambda, P_pump=P_ref,
            L=L, A_eff=self.Aeff, T_K=T_K, **kwargs)

    def _brillouinThreshold(self, lambda_ref=1550e-9):
        L = getattr(self, 'Lt', getattr(self, 'L0', None))
        return brillouin_threshold(A_eff=self.Aeff, L=L, lambda_pump=lambda_ref)

    def _checkSBSThreshold(self, P_pump, lambda_ref=1550e-9, **kwargs):
        L = getattr(self, 'Lt', getattr(self, 'L0', None))
        return check_sbs_threshold(P_pump=P_pump, A_eff=self.Aeff, L=L,
                                    lambda_pump=lambda_ref, **kwargs)

    fiber_obj.calcSpBrilNoise    = types.MethodType(_calcSpBrilNoise,    fiber_obj)
    fiber_obj.brillouinThreshold = types.MethodType(_brillouinThreshold, fiber_obj)
    fiber_obj.checkSBSThreshold  = types.MethodType(_checkSBSThreshold,  fiber_obj)
    return fiber_obj


