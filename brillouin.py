# Validation self-tests
# ─────────────────────────────────────────────────────────────────────────────

def _run_self_tests(verbose=True):
    """
    Run nine sanity checks.  Returns True if all pass.

    Checks:
      1. nu_B at 1550 nm is in [10.5, 12.0] GHz
      2. g_B Lorentzian peaks at Omega_B
      3. g_B HWHM condition: g_B(Omega_B + Gamma_B/2) = g_B_peak/2
      4. Integral of Lorentzian = pi * g_B_peak * Gamma_B / 2  (analytical)
      5. Backward L_eff = L for lossless fiber (alpha -> 0)
      6. Backward L_eff < forward L_eff for lossy fiber
      7. n_th(Omega_B, 300K) in classical limit  (kT/hbar*Omega_B)
      8. SBS threshold at 25 km SMF-28: 2-6 mW range
      9. spBril far from Brillouin line ~ 0
    """
    PASS = 'PASS'
    FAIL = 'FAIL'
    all_pass = True

    def check(name, cond, got='', exp=''):
        nonlocal all_pass
        ok = bool(cond)
        if not ok:
            all_pass = False
        if verbose:
            tag = f'[{PASS}]' if ok else f'[{FAIL}]'
            print(f'  {tag}  {name}')
            if not ok:
                print(f'         got {got}, expected {exp}')

    if verbose:
        print('=' * 58)
        print('brillouin.py self-test suite')
        print('=' * 58)

    nu_B    = brillouin_freq_shift(1550e-9)
    Omega_B = 2 * _pi * nu_B

    check('nu_B(1550nm) in [10.5, 12.0] GHz',
          10.5e9 < nu_B < 12.0e9, f'{nu_B/1e9:.3f} GHz', '~11.1 GHz')

    gB_at_peak = float(g_B_lorentzian(Omega_B, Omega_B))
    check('g_B(Omega_B) = BRIL_G_PEAK',
          abs(gB_at_peak - BRIL_G_PEAK) / BRIL_G_PEAK < 1e-10,
          gB_at_peak, BRIL_G_PEAK)

    gB_hwhm = float(g_B_lorentzian(Omega_B + BRIL_GAMMA_RAD/2, Omega_B))
    check('g_B(Omega_B + Gamma/2) = g_B_peak / 2  (HWHM)',
          abs(gB_hwhm - BRIL_G_PEAK/2) / (BRIL_G_PEAK/2) < 1e-10,
          gB_hwhm, BRIL_G_PEAK/2)

    Om_int = np.linspace(1e6, 10*Omega_B, 1_000_000)
    integral = float(simpson(g_B_lorentzian(Om_int, Omega_B), x=Om_int))
    expected = BRIL_G_PEAK * _pi * BRIL_GAMMA_RAD / 2
    check('integral(g_B) = pi * g_B_peak * Gamma_B/2  (within 0.1%)',
          abs(integral - expected) / expected < 0.001,
          f'{integral:.4e}', f'{expected:.4e}')

    Leff_0 = float(effective_length_backward(25e3, 1e-30, 1e-30))
    check('L_eff_back(alpha=0) = L  (lossless fiber)',
          abs(Leff_0 - 25e3) / 25e3 < 0.01, Leff_0, 25e3)

    alpha = float(_fiber_loss_m(1550e-9))
    Leff_b = float(effective_length_backward(25e3, alpha))
    Leff_f = (1 - np.exp(-alpha*25e3)) / alpha
    check('L_eff_back < L_eff_fwd  (extra backward loss)',
          Leff_b < Leff_f,
          f'{Leff_b/1e3:.2f} km', f'< {Leff_f/1e3:.2f} km')

    nth    = float(thermal_phonon_number(Omega_B, 300.0))
    nth_cl = float(_kB * 300.0 / (_hbar * Omega_B))
    check('n_th(Omega_B, 300K) in [500, 620]',
          500 < nth < 620, f'{nth:.1f}', '~561')
    check('n_th ~ kT/hbar*Omega  (classical, within 0.3%)',
          abs(nth - nth_cl) / nth_cl < 0.003, f'{nth:.2f}', f'{nth_cl:.2f}')

    th = brillouin_threshold(85e-12, 25e3)
    check('SBS threshold (25km SMF-28) in [2.0, 6.0] mW',
          2.0 < th['P_threshold_mW'] < 6.0,
          f"{th['P_threshold_mW']:.2f} mW", '~3.6 mW')

    res = spBril_noise_in_channel(1310e-9, 1550e-9, 1e-9, 1e-3, 25e3, 85e-12, 300)
    check('SpBril (1310->1550nm, far from Brillouin) ~ 0',
          res['backward_photon_rate'] < 1e-3,
          f"{res['backward_photon_rate']:.2e}", '~0')

    if verbose:
        print('=' * 58)
        print('ALL TESTS PASSED' if all_pass else 'SOME TESTS FAILED')
        print('=' * 58)

    return all_pass


